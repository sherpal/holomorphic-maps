package plot

import complex.Complex


/**
 * A Segment is a particular type of [[Line]] that is characterised by a starting point and endpoint.
 */
class Segment(plot: Plot, colors: (Double, Double, Double), var from: Complex, var to: Complex) extends
  Line(plot, colors, Line.segment(from, to)) {

  override def cycle: Boolean = false

  override def draw(): Unit = {
    stepPoints = Line.segment(from, to, 1000)
    super.draw()
  }
}
